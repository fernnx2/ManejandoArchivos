    public void cargarArchivo(){
        ControladorInicio ci = new ControladorInicio();
        JFileChooser filechooser = new JFileChooser();
        filechooser.setFileFilter(new FileNameExtensionFilter("Excel csv","csv"));
        int seleccion=filechooser.showOpenDialog(this);
        
        
        switch(seleccion) {
        case JFileChooser.APPROVE_OPTION:
 //seleccion√≥ abrir
         List<Equipo> equipos;   
         File archivo = filechooser.getSelectedFile();
         AdministrarArchivos aa = new AdministrarArchivos();
         String path = archivo.getPath();
         if(aa.validar(path) && aa.validarPerimisos(path)){
         equipos = aa.convertirArchivo(aa.leer_archivo(archivo.getPath()));
         this.equi = equipos;
         ci.llenarTabla(equipos, tblData);
         this.btnEnviar.setEnabled(true);
         }
         else{
         
         }
            
        break;

        case JFileChooser.CANCEL_OPTION:
 //dio click en cancelar o cerro la ventana
            System.out.println("Cerrar");
        break;

        case JFileChooser.ERROR_OPTION:
            System.out.println("Execpcion");
 //llega aqui si sucede un error
        break;
}
    
    }
    
    
